CREATE USER product WITH PASSWORD 'product' CREATEDB;
CREATE DATABASE product_db
    WITH
    OWNER = product
    ENCODING = 'UTF8'
    LC_COLLATE = 'en_US.utf8'
    LC_CTYPE = 'en_US.utf8'
    TABLESPACE = pg_default
    CONNECTION LIMIT = -1;

